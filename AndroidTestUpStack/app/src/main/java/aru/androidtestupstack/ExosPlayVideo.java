public class ExosPlayVideo extends AppCompatActivity  {
   // @BindView(R.id.exo_player_view)
    SimpleExoPlayerView exoPlayerView;
    SimpleExoPlayer exoPlayer;
    Intent mIntent;
    String mp4Url;
    private static final String TAG = "ExosPlayer";
    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_exos_play_video);
        exoPlayerView = (SimpleExoPlayerView) findViewById(R.id.exo_player_view);
        mIntent = getIntent();
        mp4Url = mIntent.getStringExtra("mp4");
        Log.e("mp4Url",mp4Url);
        try {
            BandwidthMeter bandwidthMeter = new DefaultBandwidthMeter();
            TrackSelector trackSelector = new DefaultTrackSelector(new AdaptiveTrackSelection.Factory(bandwidthMeter));
            exoPlayer = ExoPlayerFactory.newSimpleInstance(this, trackSelector);
            Uri videoURI = Uri.parse(mp4Url);
            DefaultHttpDataSourceFactory dataSourceFactory = new DefaultHttpDataSourceFactory("exoplayer_video");
            ExtractorsFactory extractorsFactory = new DefaultExtractorsFactory();
            MediaSource mediaSource = new ExtractorMediaSource(videoURI, dataSourceFactory, extractorsFactory, null, null);
            exoPlayerView.setPlayer(exoPlayer);
            exoPlayer.prepare(mediaSource);
            exoPlayer.setPlayWhenReady(true);
            
         }catch (Exception e){
            Log.e("ExosActivity"," exoplayer error "+ e.toString());
        }

    }

    @Override
    protected void onDestroy() {
        super.onDestroy();
        Log.v(TAG, "onDestroy()...");
        exoPlayer.release();
    }
}

